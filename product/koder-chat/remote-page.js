// Client-side script, built without template literals or literal backticks
// (see file header) so it can be embedded in the outer HTML string safely.
var SCRIPT = [
  "(function () {",
  "  'use strict';",
  "  var FENCE = String.fromCharCode(96, 96, 96);",
  "  var TICK = String.fromCharCode(96);",
  "  var qs = new URLSearchParams(location.search);",
  "  var token = qs.get('token');",
  "  if (token) {",
  "    try { sessionStorage.setItem('koderRemoteToken', token); } catch (e) {}",
  "    if (window.history && history.replaceState) history.replaceState(null, '', location.pathname);",
  "  } else {",
  "    try { token = sessionStorage.getItem('koderRemoteToken'); } catch (e) {}",
  "  }",
  "",
  "  var messagesEl = document.getElementById('messages');",
  "  var dotEl = document.getElementById('dot');",
  "  var wsNameEl = document.getElementById('wsName');",
  "  var modeBadgeEl = document.getElementById('modeBadge');",
  "  var bannerEl = document.getElementById('banner');",
  "  var reconnectEl = document.getElementById('reconnectScreen');",
  "",
  "  function showReconnect() {",
  "    ['header', 'banner', 'messages'].forEach(function (id) {",
  "      var el = document.getElementById(id);",
  "      if (el) el.style.display = 'none';",
  "    });",
  "    reconnectEl.className = 'show';",
  "  }",
  "",
  "  if (!token) { showReconnect(); return; }",
  "",
  "  var streamEl = null, streamRaw = '';",
  "  var tools = {};",
  "",
  "  function escapeHtml(s) {",
  "    return String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');",
  "  }",
  "  function renderRich(raw) {",
  "    var s = escapeHtml(raw);",
  "    var fenceRe = new RegExp(FENCE + '([\\\\s\\\\S]*?)(' + FENCE + '|$)', 'g');",
  "    s = s.replace(fenceRe, function (_, code) { return '<pre>' + code.replace(/^\\w*\\n/, '') + '</pre>'; });",
  "    var tickRe = new RegExp(TICK + '([^' + TICK + '\\\\n]+)' + TICK, 'g');",
  "    s = s.replace(tickRe, '<code>$1</code>');",
  "    s = s.replace(/\\*\\*([^*\\n]+)\\*\\*/g, '<b>$1</b>');",
  "    return s;",
  "  }",
  "  function clearEmpty() {",
  "    var e = messagesEl.querySelector('.empty');",
  "    if (e) e.remove();",
  "  }",
  "  function scrollBottom() {",
  "    window.scrollTo(0, document.body.scrollHeight);",
  "  }",
  "  function addMsg(cls, text) {",
  "    clearEmpty();",
  "    var el = document.createElement('div');",
  "    el.className = 'msg ' + cls;",
  "    el.textContent = text;",
  "    messagesEl.appendChild(el);",
  "    scrollBottom();",
  "    return el;",
  "  }",
  "  function endStream() {",
  "    streamEl = null;",
  "    streamRaw = '';",
  "  }",
  "  function streamText(text) {",
  "    clearEmpty();",
  "    if (!streamEl) {",
  "      streamEl = document.createElement('div');",
  "      streamEl.className = 'msg agent';",
  "      messagesEl.appendChild(streamEl);",
  "      streamRaw = '';",
  "    }",
  "    streamRaw += text;",
  "    streamEl.innerHTML = renderRich(streamRaw);",
  "    scrollBottom();",
  "  }",
  "  function addTool(t) {",
  "    endStream();",
  "    clearEmpty();",
  "    var el = document.createElement('div');",
  "    el.className = 'tool running';",
  "    el.innerHTML = '<span class=\"dot2\"></span><span class=\"title\"></span>';",
  "    el.querySelector('.title').textContent = t.title;",
  "    messagesEl.appendChild(el);",
  "    tools[t.id] = el;",
  "    scrollBottom();",
  "  }",
  "  function showPermission(m) {",
  "    var wrap = document.createElement('div');",
  "    wrap.className = 'permbar';",
  "    wrap.setAttribute('data-perm-id', String(m.id));",
  "    var pillsHtml = (m.options || []).map(function (o) {",
  "      return '<span class=\"pill\">' + escapeHtml(o.name) + '</span>';",
  "    }).join('');",
  "    wrap.innerHTML = '<div class=\"ptitle\"></div><div class=\"pills\">' + pillsHtml + '</div>' +",
  "      '<div class=\"caption\">Waiting for you in the IDE — approve or deny there.</div>';",
  "    wrap.querySelector('.ptitle').textContent = m.title;",
  "    messagesEl.appendChild(wrap);",
  "    scrollBottom();",
  "  }",
  "  function clearPermission(id) {",
  "    var el = messagesEl.querySelector('[data-perm-id=\"' + id + '\"]');",
  "    if (el) el.remove();",
  "  }",
  "",
  "  function applyEvent(m) {",
  "    switch (m.type) {",
  "      case 'user': addMsg('user', m.text); break;",
  "      case 'chunk': streamText(m.text); break;",
  "      case 'tool': addTool(m); break;",
  "      case 'toolUpdate': {",
  "        var el = tools[m.id];",
  "        if (el) el.className = 'tool ' + (m.status === 'completed' ? 'done' : m.status === 'failed' ? 'failed' : 'running');",
  "        break;",
  "      }",
  "      case 'system': endStream(); addMsg('system', m.text); break;",
  "      case 'modeChanged':",
  "        modeBadgeEl.textContent = m.mode;",
  "        if (m.auto) addMsg('system', 'Switched to ' + m.mode + ' mode.');",
  "        break;",
  "      case 'turnStart': endStream(); break;",
  "      case 'turnEnd': endStream(); break;",
  "      case 'permission': showPermission(m); break;",
  "    }",
  "  }",
  "",
  "  function renderSnapshot(snap) {",
  "    wsNameEl.textContent = snap.workspace || 'Koder';",
  "    modeBadgeEl.textContent = snap.mode || 'review';",
  "    messagesEl.innerHTML = '';",
  "    tools = {};",
  "    endStream();",
  "    var events = snap.transcript || [];",
  "    if (!events.length) {",
  "      messagesEl.innerHTML = '<div class=\"empty\"><div class=\"title\">No activity yet</div>' +",
  "        '<div class=\"hint\">Live chat updates will appear here as soon as you send a message from the IDE.</div></div>';",
  "    } else {",
  "      events.forEach(applyEvent);",
  "    }",
  "  }",
  "",
  "  function setDot(state) {",
  "    dotEl.className = state === 'open' ? '' : state;",
  "  }",
  "",
  "  var firstOpen = true;",
  "  var noConnTimer = null;",
  "",
  "  function fetchState(onDone) {",
  "    fetch('/state?token=' + encodeURIComponent(token))",
  "      .then(function (r) {",
  "        if (r.status === 401) { showReconnect(); return null; }",
  "        if (!r.ok) throw new Error('state ' + r.status);",
  "        return r.json();",
  "      })",
  "      .then(function (snap) {",
  "        if (snap) renderSnapshot(snap);",
  "        if (onDone) onDone();",
  "      })",
  "      .catch(function () {",
  "        bannerEl.textContent = 'Could not reach Koder — check you are still on the same WiFi.';",
  "        bannerEl.className = 'show';",
  "      });",
  "  }",
  "",
  "  function connect() {",
  "    fetchState(function () {",
  "      var es = new EventSource('/events?token=' + encodeURIComponent(token));",
  "      es.onopen = function () {",
  "        setDot('open');",
  "        bannerEl.className = '';",
  "        clearTimeout(noConnTimer);",
  "        if (!firstOpen) fetchState();",
  "        firstOpen = false;",
  "      };",
  "      es.onerror = function () {",
  "        setDot('reconnecting');",
  "      };",
  "      es.onmessage = function (ev) {",
  "        try {",
  "          var m = JSON.parse(ev.data);",
  "          if (m.type === 'clear') {",
  "            messagesEl.innerHTML = '<div class=\"empty\"><div class=\"title\">Chat cleared</div>' +",
  "              '<div class=\"hint\">A new chat was started in the IDE.</div></div>';",
  "            tools = {}; endStream();",
  "            return;",
  "          }",
  "          if (m.type === 'replay') {",
  "            renderSnapshot({ workspace: wsNameEl.textContent, mode: modeBadgeEl.textContent, transcript: m.events });",
  "            return;",
  "          }",
  "          applyEvent(m);",
  "        } catch (e) {}",
  "      };",
  "    });",
  "  }",
  "",
  "  connect();",
  "  noConnTimer = setTimeout(function () {",
  "    if (dotEl.className !== '') {",
  "      bannerEl.textContent = 'No connection yet. On guest/hotel WiFi your phone and laptop may be isolated from each other.';",
  "      bannerEl.className = 'show';",
  "    }",
  "  }, 20000);",
  "})();",
].join("\n");

